package secondEX;

public class IntegerReassignment {
	 public static void main(String[] args) {
	        // Declare and initialize variable
	        int myInt = 10;
	        System.out.println("Initial value: " + myInt);

	        // Reassign a new value
	        myInt = 20;
	        System.out.println("New value: " + myInt);
	   }


}
